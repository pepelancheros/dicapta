        Embracing Diversity in Audio Description: A Paradigm Shift for
        Inclusivity
      </h1>
    </section>
    <section>
      <h2>Abstract</h2>
      <p>
        This white paper explores the evolving paradigm in audio description,
        emphasizing including racial and ethnic characteristics when describing
        individuals/people in media. It aims to provide a comprehensive guide on
        best practices, highlighting the importance of representation and
        empathy, and challenging stereotypes through thoughtful and detailed
        audio descriptions.
      </p>
      <h2>Introuction</h2>
      <p>
        The art of Audio Description is based on the textual or verbal
        representation of visual elements in images, videos, movies, dances,
        plays, and so on (media, for this paper). This representation allows
        individuals who can't access or decode visual information to enjoy,
        comprehend, and actively discuss any media experience. Serving primarily
        the needs of individuals who are blind, this evolving art has enriched
        the media experience for children and adults with autism, learning
        disabilities, as well as for individuals tailoring their film and TV
        scripting skills or for those learning a different language.
      </p>
      <p>
        In multiple fields, from education to entertainment, audio description
        has traditionally skirted around explicitly mentioning race and
        ethnicity. However, society's increasing diversity awareness and
        acceptance calls for a more inclusive approach that emphasizes
        visibility and representation in media. This document examines the shift
        towards acknowledging racial and ethnic identities in audio
        descriptions, underpinning its necessity in creating a diverse and
        inclusive media landscape.
      </p>
    </section>
    <section class="blue-background">
      <h2>Background and Traditional Practices</h2>
      <p>
        Historically, audio description guidelines recommended avoiding
        mentioning race or ethnicity unless pivotal to the plot or context. This
        practice stemmed from a desire to promote inclusivity by not
        highlighting differences. However, this approach often led to
        underrepresentation and a need for more visibility for diverse groups.
      </p>
      <p>
        Describing characters is always a challenge for audio-describers. The
        racial and ethnic identity of a person is a social construction, so it
        becomes a highly subjective aspect, and its definition and
        categorization may vary according to the cultural and geographical
        context. For this reason, these characteristics have been traditionally
        omitted in audio descriptions.
      </p>
      <h3>Distinguishing Race and Ethnicity</h3>
      <p>
        Understanding the distinction between race and ethnicity is critical in
        audio descriptions. Race refers to physical attributes such as skin
        color, facial features, and hair texture and is often associated with
        biological factors. On the other hand, ethnicity is a more complex and
        multifaceted concept, encompassing cultural, linguistic, and communal
        affinities that bind individuals into distinct groups.
      </p>
      <p>
        The Merriam-Webster dictionary offers the following summary about the
        use of both words: the term race is understood today as primarily a
        sociological designation that identifies a group sharing some outward
        physical characteristics and some commonalities of culture and history,
        while ethnicity is a word for something you acquire based on where your
        family is from and the group which you share cultural, traditional, and
        familial bonds and experiences with. The end result: people may have
        racial similarity but ethnic dissimilarity.
      </p>
      <p>
        Aligned with that, it is important to emphasize that when describing,
        race may be detailed by painting with words physical attributes, but
        ethnicity belongs to a cultural interpretation made by whoever is
        describing, unintentionally adding their cultural identity to the mix.
      </p>
      <h3>Race in the traditional guidelines for audio description</h3>
      <p>
        Until recently, guidelines validated by the audio description industry
        recommended not including racial or ethnicity descriptions, except for
        specific exceptions. Regarding this subject, the
        <a
          href="https://drive.google.com/file/d/1CE_5Mgc-26QHt_q3Nzo5lhT-oc6vPkri/view"
          target="_blank"
          rel="noopener noreferrer"
          >Post Production Described Best Practices by AMI in Canada</a
        >
        states, "An identification of characters by race, ethnic origin or
        disability is not required unless there is relevancy to plot, motivation
        or background, or if meaningful information is being communicated
        visually and would be otherwise unavailable"; similarly, Dicapta's AD
        guidelines used to state, "Only when the context and subject matter
        warrant it, it is worthwhile to clarify or mention race; to do so, use
        terms such as black race, white race, or Asian features.”
      </p>
      <p>
        Even though the ideal inclusive scenario would be, precisely, not to
        talk, and discuss race differences, today, as part of an effort to reach
        that goal, highlighting, remarking, and concisely mentioning race
        differences when describing is essential for the comprehension of the
        context, and encouraging diversity awareness, as well as for promoting
        inclusion and equality.
      </p>
      <h2>The Paradigm Shift: Importance of Including Race and Ethnicity</h2>
      <img
        class="white-paper__image-left"
        src="/src/assets/images/girl-and-boy-illustration.png"
        alt="Two illustrations in one image: Image on the left. A young girl with light brown skin and long, dark, curly hair sits at a desk, reading a large book. She wears a bright yellow dress adorned with a sunflower pattern. Her eyes sparkle. The room is warmly illuminated, featuring a globe, a telescope, and posters in the background. Image on the right. A boy with dark brown skin and short, tightly coiled hair operates a toy car outside. He sports a red hoodie paired with blue jeans. He smiles. In the background are other children. A playground is in the distance."
      />
      <p>
        A shift in perspective has emerged, recognizing that omitting racial and
        ethnic descriptions can inadvertently contribute to exclusion. By
        incorporating these elements, audio descriptions can foster diversity,
        promote empathy, and challenge stereotypes while building a new society.
        This section delves into the reasons behind this paradigm shift,
        especially applied to children's media.
      </p>
      <p>
        Media can significantly influence children's perceptions and attitudes,
        including their views on race, gender, culture, and social norms. By
        ensuring that children's media is inclusive and diverse, we can
        encourage the development of a more accepting and understanding society.
        It helps in normalizing diversity and inclusivity as part of everyday
        life.
      </p>
      <q
        >Why is this discussion important? Because what is not described does
        not exist for the viewer</q
      >
      <h4>- Representation of diversity</h4>
      <p>
        Children need to see their own racial and ethnic diversity reflected in
        the media so they can feel represented and valued. By showing characters
        from different racial and ethnic backgrounds, children can identify with
        the characters and feel part of the story. This promotes a sense of
        inclusion and belonging. A good example of reactions from kids can be
        found following this link:
        <a
          href="https://www.youtube.com/watch?v=GsSJLK3WW54"
          target="_blank"
          rel="noopener noreferrer"
          >Black Kids' Heartwarming Reactions to 'The Little Mermaid'</a
        >.
      </p>
      <h4>- Promoting empathy and acceptance</h4>
      <p>
        Being exposed to racial diversity from an early age may help children
        develop empathy and understanding for people who are equal but different
        from them. By seeing and learning about different cultures and races,
        children can develop a natural view and positive attitude toward
        diversity and learn to accept and respect others.
      </p>
      <h4>- Challenging stereotypes, prejudice, and discrimination</h4>
      <p>
        Common stereotypes and prejudices are challenged by showing characters
        of different races realistically and positively. Stereotypes can limit
        children's perspectives and perpetuate prejudice and discrimination.
        <a
          href="https://www.youtube.com/watch?v=QRZPw-9sJtQ"
          target="_blank"
          rel="noopener noreferrer"
          >Follow the link to see the effects of racism on children during a
          Doll test.</a
        >
      </p>
      <q
        >“By age eight, racial attitudes are well developed and tend to stay the
        same unless a child has significant experiences and adult guidance that
        directly contradict the prevailing social attitudes” (Hindley &amp;
        Edwards, 2017)</q
      >
      <p>
        Including and showing diversity in audiovisual content accurately and
        authentically encourages an inclusive perspective, which helps break
        down negative stereotypes.
      </p>
      <h3>Children's perception of racial diversity</h3>
      <p>
        The way that children perceive racial diversity can be different
        depending on their level of understanding, previous experiences, and the
        information provided to them. This explains why audio description is
        such an important source of educational information for blind children,
        as it helps to provide them with the same experience as sighted
        children.
      </p>
      <p>
        Although blind children cannot see physical differences in people's
        race,
        <strong
          >they can develop an understanding and appreciation of racial
          diversity through other means</strong
        >, such as verbal descriptions, language, accents, and social
        interactions.
      </p>
      <h4>- Verbal descriptions</h4>
      <p>
        Children, including those who are blind, can learn about racial
        diversity through detailed verbal descriptions of people's physical
        characteristics. They can hear about variations in skin tone, hair
        color, texture, face shape, and other distinguishing features commonly
        used to describe race.
      </p>
      <h4>- Language and accents</h4>
      <p>
        Children, including those who are blind, may also notice differences in
        the sounds and accents of people's voices, which may be associated with
        a particular race or ethnicity. These elements can provide clues to
        cultural and ethnic diversity in their environment.
      </p>
      <h4>- Social interaction</h4>
      <img
        class="white-paper__image-right"
        src="/src/assets/images/african-american-boy.png"
        alt="Indoors. An African American boy with black curly hair is assembling a robot from a science kit. He is wearing a light blue t-shirt with a cartoon rocket ship design. The boy is seated at a wooden table. He has a focused expression and looks at the robot pieces on the table while he lifts a white and long piece from the table. The wall behind him is adorned with posters and a bookshelf with books."
      />
      <p>
        Children, including those who are blind, interact with different people
        in their environment, such as family members, friends, classmates, and
        community members. Through these interactions, they can learn about
        racial diversity through the experiences, perspectives, and cultural
        backgrounds of the people with whom they interact.
      </p>
      <p>
        It is important to notice that a child's experience interpreting racial
        diversity can be unique and personal. Each child has strengths,
        interests, and ways of perceiving and understanding the world. Promoting
        an inclusive environment where all identities and cultures are
        celebrated and valued is critical to supporting understanding and
        respect for racial diversity.
      </p>
      <h2>
        Recommended procedure when audio describing for the media industry
      </h2>
      <h3>
        1. Educate and introduce content creators to the basics of AD and the
        needs of children with visual disabilities from the inception of the
        series/educational content.
      </h3>
      <p>
        The video “<a
          href="https://www.youtube.com/watch?v=MEkgKtqIP2k&amp;t=26s"
          target="_blank"
          rel="noopener noreferrer"
          >Join the Audio Description Movement - The Directors Have the Floor</a
        >” by Dicapta can help introduce the concept of AD among content
        creators. Click on the name of the video to access it. You can also
        create a basic resource kit to share with them or schedule an awareness
        workshop with experts in the field.
      </p>
      <h3>2. Request character descriptions from content creators.</h3>
      <p>
        This is an editorial decision, and content creators should provide the
        Audio Description team with the specific details they wish to highlight
        for the audience. Dicapta has created a form for that purpose. Follow
        the link to review
        <a
          href="https://docs.google.com/forms/d/e/1FAIpQLSdfUl-sDMw5o0Rx2oauX1YwgIU51l_Fn2eD_XxWpsJMwU1e7g/viewform"
          target="_blank"
          rel="noopener noreferrer"
          >the character's visual details to consider when describing</a
        >, and feel free to use it as a guide.
      </p>
      <h3>
        3. Consult with experts or represented community members to ensure
        accurate description.
      </h3>
      <p>
        Consulting with people with first-hand knowledge or experience related
        to race and ethnicity will improve the final product. Their input can
        provide valuable information and help avoid potential stereotypes or
        bias. See references for some examples.
      </p>
      <h3>
        4. Assess the significance of each character within the specific context
        to determine which aspects should be described and which should not.
      </h3>
      <p>
        Evaluating how the character's physical characteristics contribute to
        their identity and the story is critical. This will help guide the
        decision-making process on what aspects to emphasize in the Audio
        Description.
      </p>
      <h3>
        5. Collaboration between the content creators and the AD team will
        ensure the accurate portrayal of race and ethnicity in the audiovisual
        content.
      </h3>
      <p>
        Solicit feedback and review of the Audio Description script from the
        content creator to facilitate this process.
      </p>
      <h3>
        6. Testing the material with the target audience to make necessary
        changes or adjustments. Seek feedback and engage in dialogue with
        consumers. For example, blind children, their families, and the broader
        blind community.
      </h3>
      <p>
        It is important to work together on this last step, which will provide
        additional feedback on race and ethnicity and the quality of the Audio
        Description produced. Dicapta actively involves blind children, their
        families, and representatives of the blind community in developing and
        improving audio description. Their perspectives have been invaluable for
        the production team and will be invaluable to the content creator to
        ensure that the media content is inclusive and meets their needs.
      </p>
